char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	l;

	l = 0;
	i = 0;
	while (dest[l])
		l++;
	while (src[i])
	{
		dest[l] = src[i];
		i++;
		l++;
	}
	dest[l] = '\0';
	return (dest);
}
