int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	bulundu;

	bulundu = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 65 || str[i] > 90)
			return (0);
		i++;
	}
	return (1);
}
